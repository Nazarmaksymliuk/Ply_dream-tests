package org.example.Models;

public class Warehouse {
    public String name;
    public String address;
    public String city;
    public String state;
    public String zip;
    public String apt;

    public Warehouse(String name, String address, String city, String state, String zip, String apt) {
        this.name = name;
        this.address = address;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.apt = apt;
    }
}
